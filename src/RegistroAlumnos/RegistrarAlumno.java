    /**
     * Da el formato a la tabla en donde se muestran los registros y también
     * llena la tabla con los datos.
     */
    public void CargarAlumnos() {
        String Columnas[] = {"NOMBRE", "MATRICULA"};
        this.alumnos = AlumnosDAO.getAlumnos();
        DefaultTableModel model = new DefaultTableModel(new Object[0][0], Columnas);
        if (this.alumnos != null) {
            for (Alumno ad : this.alumnos) {
                Object fila[] = new Object[2];
                fila[0] = ad.getNombre();
                fila[1] = ad.getMatricula();
                model.addRow(fila);
            }
            this.tablaAlumnos.setModel(model);
        }
    }

