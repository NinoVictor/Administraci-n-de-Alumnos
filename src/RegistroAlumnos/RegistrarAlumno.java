    /**
     * Recibe los datos ingresados por el usuario y los manda a otro método para
     * que sean guardados.
     *
     * @param nuevo verifica si los datos que se van a ingresar son de un alumno
     * nuevo o uno ya regstrado.
     * @param indice recibe la ubicación del registro en la tabla.
     */
    public void GuardarAlumno(boolean nuevo, int indice) {
        alum = new Alumno();
        alum.setNombre(nombreAlumno.getText());
        alum.setMatricula(matriculaAlumno.getText());
        if (nuevo) {
            if (AlumnosDAO.guardarAlumno(alum)) {
                JOptionPane.showMessageDialog(null, "Alumno "
                        + "Guardado Correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "Alumno  NO "
                        + "Guardado Correctamente");
            }
        } else {
            AlumnosDAO.actualizarAdministrativo(alumnos, indice, alum);
            JOptionPane.showMessageDialog(null, "Alumno "
                    + "Actualizado Correctamente");
        }
    }

    /**
     * Verifica si se selecciono un registro de la tabla y después lo elimina.
     */
    public void EliminarAlumno() {
        if (tablaAlumnos.getSelectedRow() >= 0) {
            int respuesta = JOptionPane.showOptionDialog(this, "¿Estas seguro "
                    + "que deseas eliminar el alumno?",
                    "Cerrar sesion",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE, null, null, null);
            if (respuesta == JOptionPane.YES_OPTION) {
                AlumnosDAO.eliminarAlumno(alumnos, tablaAlumnos.getSelectedRow());
                CargarAlumnos();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona el contacto "
                    + "a eliminar de la tabla");
        }
    }

    /**
     * Verifica si se selecciono un registro en la tabla y muestra los datos del
     * alumno a editar en el formulario.
     */
    private void EditarAlumno() {
        if (tablaAlumnos.getSelectedRow() >= 0) {
            indice = tablaAlumnos.getSelectedRow();
            //GuardarAlumno(false,tablaAlumnos.getSelectedRow());
            Formulario.setVisible(true);
            this.btnGuardarModi.setVisible(true);
            this.btnGuardar.setVisible(false);
            alum = alumnos.get(indice);
            nombreAlumno.setText(alum.getNombre());
            matriculaAlumno.setText(alum.getMatricula());
            //CargarAlumnos();
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona el "
                    + "contenido a editar de la tabla");
        }
    }

